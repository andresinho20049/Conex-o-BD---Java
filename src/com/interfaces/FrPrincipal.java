    public void select_jtable(){
            int linha = jTable1.getSelectedRow();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            
            switch (form) {
                case 0:
                    TabelaCDsBean c = new TabelaCDsBean();
                    TabelaArtistaBean ar = new TabelaArtistaBean();
                    TabelaGeneroBean ge = new TabelaGeneroBean();
                    FrCDs cd = new FrCDs();
                    c.setId((int) jTable1.getValueAt(linha, 0));
                    c.setTitulo(jTable1.getValueAt(linha, 1).toString());
                    c.setPreco((double) jTable1.getValueAt(linha, 2));
                    ar.setId((int) jTable1.getValueAt(linha, 3));
                    ar.setNome(jTable1.getValueAt(linha, 4).toString());
                    ge.setId((int) jTable1.getValueAt(linha, 5));
                    ge.setNome(jTable1.getValueAt(linha, 6).toString());
                    c.setArtista(ar);
                    c.setGenero(ge);
                    
                    cd.setVisible(true);
                    cd.RetornaDados(c);
                break;
                case 1:
                    TabelaArtistaBean a = new TabelaArtistaBean();
                    FrArtista artista = new FrArtista();
                    a.setId((int) jTable1.getValueAt(linha, 0));
                    a.setNome(jTable1.getValueAt(linha, 1).toString());
                    artista.setVisible(true);
                    artista.RetornaDados(a);
                break;
                case 2:
                    TabelaGeneroBean g = new TabelaGeneroBean();
                    FrGenero genero = new FrGenero();
                    g.setId((int) jTable1.getValueAt(linha, 0));
                    g.setNome(jTable1.getValueAt(linha, 1).toString());
                    genero.setVisible(true);
                    genero.RetornaDados(g);
                break;
                default:
                    JOptionPane.showMessageDialog(null, "Op√ß√£o Invalida!!!");
            }
            
    }
    
    
