    public void RetornaDados(TabelaCDsBean c){
        painel1.setVisible(true);
        btn_edit.setEnabled(true);
        btn_excluir.setEnabled(true);
        btn_cad.setEnabled(false);
        cd_txt.setText(String.valueOf(c.getId()));
        nm_txt.setText(c.getTitulo());
        vr_txt.setText(String.valueOf(c.getPreco()));
        g_box.getModel().setSelectedItem(c.getGenero());
        g_box2.getModel().setSelectedItem(c.getArtista());
    }
    
    
    
    
    private void btn_cadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadActionPerformed
        // TODO add your handling code here:
        
        CDs_dao c = new CDs_dao();
        TabelaCDsBean p = new TabelaCDsBean();
        
        TabelaGeneroBean ge = (TabelaGeneroBean) g_box.getSelectedItem();
        TabelaArtistaBean au = (TabelaArtistaBean) g_box2.getSelectedItem();

        //p.setId(Integer.parseInt(id_txt.getText()));
        p.setTitulo(nm_txt.getText());
        p.setPreco(Double.parseDouble(vr_txt.getText()));
        p.setGenero(ge);
        p.setArtista(au);
        c.create(p);

        nm_txt.setText("");
        //isqn_txt.setText("");
        vr_txt.setText("");
        cd_txt.setText("");
        g_box.setSelectedIndex(0);
        g_box2.setSelectedIndex(0);
        
    }//GEN-LAST:event_btn_cadActionPerformed

