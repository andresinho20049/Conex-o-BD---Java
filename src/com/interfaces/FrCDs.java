    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        CDs_dao c = new CDs_dao();
        TabelaCDsBean p = new TabelaCDsBean();
        
        TabelaGeneroBean ge = (TabelaGeneroBean) g_box.getSelectedItem();
        TabelaArtistaBean au = (TabelaArtistaBean) g_box2.getSelectedItem();

        p.setId(Integer.parseInt(cd_txt.getText()));
        p.setTitulo(nm_txt.getText());
        p.setPreco(Double.parseDouble(vr_txt.getText()));
        p.setGenero(ge);
        p.setArtista(au);
        c.update(p);

        nm_txt.setText("");
        //isqn_txt.setText("");
        vr_txt.setText("");
        cd_txt.setText("");
        g_box.setSelectedIndex(0);
        g_box2.setSelectedIndex(0);
        painel1.setVisible(false);
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        // TODO add your handling code here:
        CDs_dao c = new CDs_dao();
        TabelaCDsBean p = new TabelaCDsBean();

        p.setId(Integer.parseInt(cd_txt.getText()));
        c.delete(p);

        nm_txt.setText("");
        //isqn_txt.setText("");
        vr_txt.setText("");
        cd_txt.setText("");
        g_box.setSelectedIndex(0);
        g_box2.setSelectedIndex(0);
        painel1.setVisible(false);
    }//GEN-LAST:event_btn_excluirActionPerformed

